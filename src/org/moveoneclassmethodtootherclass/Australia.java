package org.moveoneclassmethodtootherclass;
//child class
public class Australia {
	
	public void sydney() {
		System.out.println("capital of africa");
	} 
public void melbourne() {
		System.out.println("sports arena");
	}
public static void main(String[] args) {
	Australia a = new Australia();
	a.sydney();
	a.melbourne();
	Africa b = new Africa();
	b.southAfrica();
	b.northAfrica();
}
}
